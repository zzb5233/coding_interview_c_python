// 面试题4：二维数组中的查找
// 题目：在一个二维数组中，每一行都按照从左到右递增的顺序排序，每一列都按
// 照从上到下递增的顺序排序。请完成一个函数，输入这样的一个二维数组和一个
// 整数，判断数组中是否含有该整数。



bool find(int *matrix, int rows, int columns, int number)
{
	bool found = false;
	int row, column;
	
	if(NULL == matrix || 0 >= rows || 0 >= columns){
		return false;
	}
	
	row = 0;
	column = columns - 1;
	while(row < rows && column >= 0){
		if(matrix[row * columns + column] == number){
			found = true;
			break;
		}
		else if(matrix[row * columns + column] > number){
			--column;
		}
		else{
			++row;
		}
			
	}
	return found;
}


// ====================测试代码====================
void test(char* test_name, int* matrix, int rows, int columns, int number, bool expected)
{
    if(test_name != NULL){
		printf("%s begins: ", test_name);
	}
        

    bool result = find(matrix, rows, columns, number);
    if(result == expected)
        printf("Passed.\n");
    else
        printf("Failed.\n");
}

